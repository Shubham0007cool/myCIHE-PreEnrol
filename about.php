<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Get the scholarships section and button
        const scholarshipsSection = document.querySelector('.scholarships-section');
        const viewButton = document.querySelector('a[href="#scholarships"] button');
        
        // Initially hide the scholarships section
        scholarshipsSection.style.display = 'none';
        
        // Toggle visibility when button is clicked
        viewButton.addEventListener('click', function(e) {
            e.preventDefault(); // Prevent default anchor behavior
            
            if (scholarshipsSection.style.display === 'none') {
                scholarshipsSection.style.display = 'block';
                
                // Smooth scroll to the section
                scholarshipsSection.scrollIntoView({ behavior: 'smooth' });
                
                // Change button text when shown
                viewButton.textContent = 'Hide Scholarships';
            } else {
                scholarshipsSection.style.display = 'none';
                
                // Change button text when hidden
                viewButton.textContent = 'View Scholarships';
            }
        });
        
        // Also handle direct anchor links (if someone shares the URL with #scholarships)
        window.addEventListener('hashchange', function() {
            if (window.location.hash === '#scholarships') {
                scholarshipsSection.style.display = 'block';
                viewButton.textContent = 'Hide Scholarships';
                scholarshipsSection.scrollIntoView({ behavior: 'smooth' });
            }
        });
        
        // Check on page load if the hash is present
        if (window.location.hash === '#scholarships') {
            scholarshipsSection.style.display = 'block';
            viewButton.textContent = 'Hide Scholarships';
        }
    });
    </script>

